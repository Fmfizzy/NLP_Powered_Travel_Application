def add_location_scores(score_list, location):
    location_dict = location_dicts.get(location, None)
    
    if location_dict is not None:
        updated_scores = []
        for label, score in score_list:
            if label in location_dict:
                updated_scores.append((label, score + location_dict[label]))
            else:
                updated_scores.append((label, score))
        updated_scores.sort(key=lambda x: x[1], reverse=True)
        return updated_scores[:3]
    else:
        return score_list[:3]